# ── WRITE INTENTS ────────────────────────────────────────────────────────────

def main():
    with open(INTENTS_FILE, "r", encoding="utf-8") as f:
        existing = json.load(f)

    existing_ids = {i["id"] for i in existing}
    new_count = 0
    for intent in OSS_INTENTS + IBOSS_INTENTS:
        if intent["id"] not in existing_ids:
            existing.append(intent)
            existing_ids.add(intent["id"])
            new_count += 1
            print(f"  + {intent['id']}")
        else:
            print(f"  = {intent['id']} (already exists)")

    with open(INTENTS_FILE, "w", encoding="utf-8") as f:
        json.dump(existing, f, indent=2, ensure_ascii=False)
        f.write("\n")

    print(f"\nDone. Added {new_count} new intents. Total: {len(existing)} intents.")

if __name__ == "__main__":
    main()
