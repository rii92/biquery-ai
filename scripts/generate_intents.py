OSS_RAPOR_STAF_SQL = """WITH STATS_STAF AS (
    SELECT 
        UPPER(TRIM(VERIFIKATOR)) AS NAMA_STAF,
        COUNT(NO_PERMOHONAN) AS TOTAL_KERJA,
        SUM(CASE WHEN KATEGORI_STATUS <> 'DALAM PROSES' THEN 1 ELSE 0 END) AS TOTAL_LOLOS,
        SUM(CASE WHEN TO_CHAR(TGL_STATUS_TERAKHIR, 'HH24') = '00' OR TO_CHAR(TGL_STATUS_TERAKHIR, 'HH24MI') BETWEEN '0800' AND '1630' THEN 1 ELSE 0 END) AS TOTAL_DALAM_JAM_KERJA,
        SUM(CASE WHEN STATUS_PENCAPAIAN_SLA = 'SESUAI SLA' THEN 1 ELSE 0 END) AS TOTAL_MEMENUHI_SLA
    FROM US_DWH.BI_T_IZIN_OSS
    WHERE VERIFIKATOR IS NOT NULL
      AND (UPPER(JABATAN_VERIFIKATOR) LIKE '%VERIFIKATOR%' OR UPPER(JABATAN_VERIFIKATOR) LIKE '%STAF%')
      AND {pilih_izin}
      AND {filter_tahun}
      AND {filter_bulan}
      AND {rentang_tgl_masuk}
    GROUP BY UPPER(TRIM(VERIFIKATOR))
)
SELECT 
    NAMA_STAF AS NAMA_STAF,
    COALESCE(ROUND(
        (0.4 * (TOTAL_LOLOS / NULLIF(TOTAL_KERJA, 0)) * 100) + 
        (0.3 * (TOTAL_DALAM_JAM_KERJA / NULLIF(TOTAL_KERJA, 0)) * 100) + 
        (0.3 * (TOTAL_MEMENUHI_SLA / NULLIF(TOTAL_KERJA, 0)) * 100)
    , 2), 0) AS SKOR_AKHIR,
    COALESCE(ROUND((TOTAL_LOLOS / NULLIF(TOTAL_KERJA, 0)) * 100, 2), 0) AS NILAI_PERFORMA,
    COALESCE(ROUND((TOTAL_DALAM_JAM_KERJA / NULLIF(TOTAL_KERJA, 0)) * 100, 2), 0) AS NILAI_PRODUKTIVITAS,
    COALESCE(ROUND((TOTAL_MEMENUHI_SLA / NULLIF(TOTAL_KERJA, 0)) * 100, 2), 0) AS NILAI_SLA,
    TOTAL_KERJA AS TOTAL_DOKUMEN_KERJA
FROM STATS_STAF
ORDER BY SKOR_AKHIR DESC"""

