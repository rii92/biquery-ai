IBOSS_RAPOR_STAF_SQL = """WITH DOC_SLA AS (
    SELECT ID_IZIN, SUM(DIFFERENCE_TIME) AS TOTAL_WAKTU_DETIK
    FROM US_OSS.T_LOG_LICENSING
    WHERE DIFFERENCE_TIME IS NOT NULL
    GROUP BY ID_IZIN
),
STAFF_LOGS AS (
    SELECT 
        U.NAME,
        L.LOG_ID,
        TX_PERMOHONAN.STATUS_IZIN,
        CASE 
            WHEN TO_CHAR(L.ACTION_TIME, 'DY', 'NLS_DATE_LANGUAGE=ENGLISH') NOT IN ('SAT', 'SUN') 
            AND TO_NUMBER(TO_CHAR(L.ACTION_TIME, 'HH24MI')) BETWEEN 800 AND 1630 
            THEN 1 ELSE 0 
        END AS IS_JAM_KERJA,
        CASE 
            WHEN (
                CAST(NVL(TX_PERMOHONAN.LAST_PROSES, SYSDATE) AS DATE)
                -
                (CASE 
                    WHEN (CAST(TX_PERMOHONAN.TGLBAYAR AS DATE) - TRUNC(CAST(TX_PERMOHONAN.TGLBAYAR AS DATE))) > (16.5 / 24) 
                         THEN TRUNC(CAST(TX_PERMOHONAN.TGLBAYAR AS DATE)) + 1 + (8 / 24)
                    WHEN (CAST(TX_PERMOHONAN.TGLBAYAR AS DATE) - TRUNC(CAST(TX_PERMOHONAN.TGLBAYAR AS DATE))) < (8 / 24) 
                         THEN TRUNC(CAST(TX_PERMOHONAN.TGLBAYAR AS DATE)) + (8 / 24)
                    ELSE CAST(TX_PERMOHONAN.TGLBAYAR AS DATE)
                END)
            ) * 24 <= 24 THEN 1 
            WHEN D.TOTAL_WAKTU_DETIK <= 86400 THEN 1 
            ELSE 0 
        END AS IS_MEMENUHI_SLA
    FROM US_OSS.T_LOG_LICENSING L
    JOIN US_OSS.TX_USERS U ON L.USER_ID = U.USER_ID
    JOIN US_OSS.TX_PERMOHONAN ON L.ID_IZIN = TO_CHAR(TX_PERMOHONAN.SEQREQ)
    LEFT JOIN DOC_SLA D ON L.ID_IZIN = D.ID_IZIN
    LEFT JOIN US_OSS.TM_LAYAN LYN ON TX_PERMOHONAN.IDLAYAN = LYN.IDLAYAN
    LEFT JOIN US_OSS.TM_ROLE R ON TX_PERMOHONAN.STATUS_IZIN = R.KD_ROLE
    WHERE L.USER_ID != 0 AND L.DIFFERENCE_TIME IS NOT NULL
      AND U.ROLE_ID IN ('0203020102','0203020202')
      AND TX_PERMOHONAN.STATUSINVOICE = 1
      AND {rentang_waktu}
      AND {pilih_tahun}
      AND {pilih_bulan}
      AND {pilih_role}
      AND {pilih_izin_iboss}
),
STAFF_AGGR AS (
    SELECT 
        NAME,
        COUNT(LOG_ID) AS TOTAL_KERJA,
        SUM(CASE WHEN STATUS_IZIN NOT IN ('0203020102','0203020202') THEN 1 ELSE 0 END) AS TOTAL_LOLOS,
        SUM(IS_JAM_KERJA) AS TOTAL_DALAM_JAM_KERJA,
        SUM(IS_MEMENUHI_SLA) AS TOTAL_MEMENUHI_SLA
    FROM STAFF_LOGS
    GROUP BY NAME
)
SELECT 
    NAME AS NAMA_STAF,
    ROUND(
        (0.4 * (TOTAL_LOLOS / NULLIF(TOTAL_KERJA, 0)) * 100) + 
        (0.3 * (TOTAL_DALAM_JAM_KERJA / NULLIF(TOTAL_KERJA, 0)) * 100) + 
        (0.3 * (TOTAL_MEMENUHI_SLA / NULLIF(TOTAL_KERJA, 0)) * 100)
    , 2) AS SKOR_AKHIR,
    ROUND((TOTAL_LOLOS / NULLIF(TOTAL_KERJA, 0)) * 100, 2) AS NILAI_PERFORMA,
    ROUND((TOTAL_DALAM_JAM_KERJA / NULLIF(TOTAL_KERJA, 0)) * 100, 2) AS NILAI_PRODUKTIVITAS,
    ROUND((TOTAL_MEMENUHI_SLA / NULLIF(TOTAL_KERJA, 0)) * 100, 2) AS NILAI_SLA
FROM STAFF_AGGR
ORDER BY SKOR_AKHIR DESC"""

