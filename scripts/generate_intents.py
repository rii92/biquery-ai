def convert_sql(sql: str, source: str) -> str:
    """Convert Metabase [[ {{param}} ]] syntax to {param} syntax."""
    import re
    # Remove [[ ... ]] wrappers and replace {{param}} with {param}
    # First replace all {{PARAM}} or {{param}} with {param} (lowercased)
    sql = re.sub(r'\{\{(\w+)\}\}', lambda m: '{' + m.group(1).lower() + '}', sql)
    # Then remove [[ and ]] markers (they wrap optional clauses in Metabase)
    sql = re.sub(r'\[\[\s*AND\s+', 'AND ', sql)
    sql = re.sub(r'\[\[\s*', '', sql)
    sql = re.sub(r'\s*\]\]', '', sql)
    return sql

